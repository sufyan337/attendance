<h1>Teachers</h1>
<h2>How to create teachers</h2>
<ol>
	<li>Create new WordPress user.</li>
	<li>Enter all required fields and set role as teacher.</li>
	<li>Click on add user.</li>
	<li>A new teacher profile will be created.</li>
</ol>
<h2>Edit Teacher details</h2>
<ol>
	<li>Go to Teachers page in admin panel.</li>
	<li>In list of teachers click on Edit for respective teacher.</li>
	<li>Edit options will appear.</li>
</ol>

<h1>Students</h1>
<h2>Create Students</h2>
<ol>
	<li>Go to Students page.</li>
	<li>Enter student name select class.</li>
	<li>Click on add button.</li>
</ol>
<h1>Classes</h1>
<ol>
	<li>Go to Classes page.</li>
	<li>Enter class name and click on Add button.</li>
</ol>